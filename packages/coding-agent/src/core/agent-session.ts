	private _refreshToolRegistry(options?: { activeToolNames?: string[] }): void {
		const previousRegistryNames = new Set(this._toolRegistry.keys());
		const previousActiveToolNames = this.getActiveToolNames();
		const allowedToolNames = this._allowedToolNames;
		const excludedToolNames = this._excludedToolNames;
		const isAllowedTool = (name: string): boolean =>
			(!allowedToolNames || allowedToolNames.has(name)) && !excludedToolNames?.has(name);

		const allCustomTools = this._customTools
			.map((definition) => ({
				definition,
				sourceInfo: createSyntheticSourceInfo(`<sdk:${definition.name}>`, { source: "sdk" }),
			}))
			.filter((tool) => isAllowedTool(tool.definition.name));
		const definitionRegistry = new Map<string, ToolDefinitionEntry>(
			Array.from(this._baseToolDefinitions.entries())
				.filter(([name]) => isAllowedTool(name))
				.map(([name, definition]) => [
					name,
					{
						definition,
						sourceInfo: createSyntheticSourceInfo(`<builtin:${name}>`, { source: "builtin" }),
					},
				]),
		);
		for (const tool of allCustomTools) {
			definitionRegistry.set(tool.definition.name, {
				definition: tool.definition,
				sourceInfo: tool.sourceInfo,
			});
		}
		this._toolDefinitions = definitionRegistry;
		this._toolPromptSnippets = new Map(
			Array.from(definitionRegistry.values())
				.map(({ definition }) => {
					const snippet = this._normalizePromptSnippet(definition.promptSnippet);
					return snippet ? ([definition.name, snippet] as const) : undefined;
				})
				.filter((entry): entry is readonly [string, string] => entry !== undefined),
		);
		this._toolPromptGuidelines = new Map(
			Array.from(definitionRegistry.values())
				.map(({ definition }) => {
					const guidelines = this._normalizePromptGuidelines(definition.promptGuidelines);
					return guidelines.length > 0 ? ([definition.name, guidelines] as const) : undefined;
				})
				.filter((entry): entry is readonly [string, string[]] => entry !== undefined),
		);
		const toolContext = (): ToolExecutionContext => ({ cwd: this._cwd, model: this.model });
		const wrappedCustomTools = allCustomTools.map(({ definition }) => wrapToolDefinition(definition, toolContext));
		const wrappedBuiltInTools = Array.from(this._baseToolDefinitions.values())
			.filter((definition) => isAllowedTool(definition.name))
			.map((definition) => wrapToolDefinition(definition, toolContext));

		const toolRegistry = new Map(wrappedBuiltInTools.map((tool) => [tool.name, tool]));
		for (const tool of wrappedCustomTools as AgentTool[]) {
			toolRegistry.set(tool.name, tool);
		}
		this._toolRegistry = toolRegistry;

		const nextActiveToolNames = (
			options?.activeToolNames ? [...options.activeToolNames] : [...previousActiveToolNames]
		).filter((name) => isAllowedTool(name));

		if (allowedToolNames) {
			for (const toolName of this._toolRegistry.keys()) {
				if (allowedToolNames.has(toolName)) {
					nextActiveToolNames.push(toolName);
				}
			}
		} else if (!options?.activeToolNames) {
			for (const toolName of this._toolRegistry.keys()) {
				if (!previousRegistryNames.has(toolName)) {
					nextActiveToolNames.push(toolName);
				}
			}
		}

		this.setActiveToolsByName([...new Set(nextActiveToolNames)]);
	}

	private _buildRuntime(options: { activeToolNames?: string[] }): void {
		const autoResizeImages = this.settingsManager.getImageAutoResize();
		const shellCommandPrefix = this.settingsManager.getShellCommandPrefix();
		const shellPath = this.settingsManager.getShellPath();
		const baseToolDefinitions: Record<string, ToolDefinition<any, any>> = this._baseToolsOverride
			? Object.fromEntries(
					Object.entries(this._baseToolsOverride).map(([name, tool]) => [
						name,
						createToolDefinitionFromAgentTool(tool),
					]),
				)
			: createAllToolDefinitions(this._cwd, {
					read: { autoResizeImages },
					bash: { commandPrefix: shellCommandPrefix, shellPath },
				});
		if (!this._baseToolsOverride && this.settingsManager.getSmartModelUsage()) {
			baseToolDefinitions.codeify_model = createCodeifyModelToolDefinition(this._cwd, this._modelRuntime, {
				imagesBlocked: () => this.settingsManager.getBlockImages(),
				autoResizeImages: () => this.settingsManager.getImageAutoResize(),
				getDelegatedToolNames: () =>
					[...this._toolRegistry.keys()].filter((name) => name !== "codeify_model" && name !== "computer"),
				createDelegatedTools: (computerPolicy) => {
					const tools = [...this._toolRegistry.values()].filter(
						(tool) => tool.name !== "codeify_model" && tool.name !== "computer",
					);
					if (!computerPolicy) return { tools };
					const computer = createComputerToolController({ policy: computerPolicy });
					tools.push(wrapToolDefinition(computer.definition, () => ({ cwd: this._cwd, model: this.model })));
					return { tools, dispose: () => computer.dispose() };
				},
			});
		}

		this._baseToolDefinitions = new Map(
			Object.entries(baseToolDefinitions).map(([name, tool]) => [name, tool as ToolDefinition]),
		);

		const defaultActiveToolNames = this._baseToolsOverride
			? Object.keys(this._baseToolsOverride)
			: ["read", "bash", "edit", "write"];
		const baseActiveToolNames = [...(options.activeToolNames ?? defaultActiveToolNames)];
		if (this.settingsManager.getSmartModelUsage()) baseActiveToolNames.push("codeify_model");
		this._refreshToolRegistry({ activeToolNames: baseActiveToolNames });
	}

